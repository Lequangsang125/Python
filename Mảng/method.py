# | Method      | Chức năng chính                                              |
# | ----------- | ------------------------------------------------------------ |
# | `append()`  | Thêm phần tử vào cuối danh sách                              |
# | `clear()`   | Xóa toàn bộ phần tử trong danh sách                          |
# | `copy()`    | Trả về bản sao của danh sách                                 |
# | `count()`   | Đếm số lần xuất hiện của một giá trị                         |
# | `extend()`  | Nối thêm các phần tử từ iterable khác                        |
# | `index()`   | Trả về vị trí đầu tiên của giá trị                           |
# | `insert()`  | Thêm phần tử tại vị trí chỉ định                             |
# | `pop()`     | Xóa và trả về phần tử tại vị trí chỉ định (mặc định là cuối) |
# | `remove()`  | Xóa phần tử đầu tiên có giá trị chỉ định                     |
# | `reverse()` | Đảo ngược thứ tự các phần tử                                 |
# | `sort()`    | Sắp xếp danh sách tăng dần (hoặc tùy chỉnh)                  |
