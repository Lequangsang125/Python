# | Phương thức              | Mô tả ngắn gọn                                                    |
# | ------------------------ | ----------------------------------------------------------------- |
# | `clear()`                | Xóa toàn bộ các phần tử trong từ điển                             |
# | `copy()`                 | Trả về bản sao của từ điển                                        |
# | `fromkeys(keys, value)`  | Tạo từ điển mới với danh sách `keys`, tất cả cùng giá trị `value` |
# | `get(key)`               | Trả về giá trị của `key`, không lỗi nếu không tồn tại             |
# | `items()`                | Trả về danh sách các cặp `(key, value)` dạng tuple                |
# | `keys()`                 | Trả về danh sách các khóa (key)                                   |
# | `pop(key)`               | Xóa phần tử có `key` và trả về giá trị của nó                     |
# | `popitem()`              | Xóa và trả về cặp `(key, value)` cuối cùng được thêm vào          |
# | `setdefault(key, value)` | Trả về giá trị của `key`. Nếu không có thì thêm `key` với `value` |
# | `update(other_dict)`     | Cập nhật từ điển hiện tại bằng các cặp từ `other_dict`            |
# | `values()`               | Trả về danh sách các giá trị (value)                              |
