# GÁN NHIỀU GIÁ TRỊ
# Cho phép gán giá trị cho nhiều biến trong một dòng
x , y , z = "orange", "banana" , "tomato"
print(x)
print(y)
print(z)
# Lưu ý đảm bảo số lượng biến khớp với số lượng giá trị,k thì lỗi

#MỘT GIÁ TRỊ CHO NHIỀU BIẾN
x = y = z = "orange"
print(x)
print(y)
print(z)

#UNPACK - GIẢI NÉN MẢNG
fruits = ['apple','banana','cherry']
x,y,z = fruits
print(x)
print(y)
print(z)